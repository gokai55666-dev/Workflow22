from flask import Flask, request, jsonify
from ollama_comfyui_agent import LLMComfyUIAgent
import threading

app = Flask(__name__)
agent = LLMComfyUIAgent()

@app.route('/chat', methods=['POST'])
def chat():
    data = request.json
    message = data.get('message', '')
    model = data.get('model', 'llama3.2')
    
    response = agent.chat(message, model)
    return jsonify({'response': response})

@app.route('/generate', methods=['POST'])
def generate():
    data = request.json
    prompt = data.get('prompt', '')
    width = data.get('width', 512)
    height = data.get('height', 512)
    
    result = agent.comfy.generate_image(prompt, width=width, height=height)
    return jsonify(result)

@app.route('/status', methods=['GET'])
def status():
    return jsonify({
        'comfyui': agent.comfy.get_workflow() is not None,
        'models': agent.comfy.available_models
    })

if __name__ == '__main__':
    app.run(host='0.0.0.0', port=5000)
